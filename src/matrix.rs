use fmt::Display;
use std::fmt;
use std::fmt::Formatter;

#[derive(Debug, Clone, PartialEq)]
pub struct Matrix {
    data: Vec<u8>,
}

impl Matrix {
    pub fn join_matrix(&mut self, matrix: &Matrix) {
        for index in 0..matrix.data.len() {
            if matrix.data[index] != 0 {
                self.data[index] = matrix.data[index]
            }
        }
    }

    // Shift all items by x & y offset
    pub fn shift_matrix(self, dx: usize, dy: usize) -> Self {
        let buffer = self.data;

        let mut output_buffer = Vec::from(EMPTY_MATRIX);

        for row in 0..MATRIX_HEIGHT {
            for column in 0..MATRIX_WIDTH {
                let src_idx = row * MATRIX_WIDTH + column;

                let new_col = column + dx;
                let new_row = row + dy;

                if new_col < MATRIX_WIDTH && new_row < MATRIX_HEIGHT {
                    output_buffer[new_row * MATRIX_WIDTH + new_col] = buffer[src_idx]
                }
            }
        }

        Matrix {
            data: output_buffer,
        }
    }

    pub fn from_picture(picture: Vec<u8>, width: usize, height: usize) -> Self {
        let mut output = Vec::from(EMPTY_MATRIX);

        for row in 0..height {
            for column in 0..width {
                output[row * MATRIX_WIDTH + column] = picture[row * width + column]
            }
        }
        Matrix { data: output }
    }

    pub fn get_el(&self, row: usize, col: usize) -> u8 {
        self.data[row * MATRIX_WIDTH + col]
    }

    fn get_row(&self, row: u8) -> [u8; MATRIX_WIDTH] {
        let mut target_row = [0u8; MATRIX_WIDTH];
        for col in 0u8..9 {
            target_row[col as usize] = self.get_el(row as usize, col as usize);
        }
        target_row
    }

    pub fn get_col(&self, col: usize) -> [u8; MATRIX_HEIGHT] {
        let mut target_col = [0u8; MATRIX_HEIGHT];
        for i in 0..MATRIX_HEIGHT {
            target_col[i] = self.get_el(i, col);
        }
        target_col
    }
}

impl Default for Matrix {
    fn default() -> Self {
        Self {
            data: EMPTY_MATRIX.to_vec(),
        }
    }
}

#[derive(Debug)]
pub enum TryFromMatrixError {
    IncorrectBufferSize,
}

impl TryFrom<&[u8]> for Matrix {
    type Error = TryFromMatrixError;

    fn try_from(value: &[u8]) -> Result<Self, Self::Error> {
        if value.len() != MATRIX_ITEM_COUNT {
            return Err(TryFromMatrixError::IncorrectBufferSize);
        };

        return Ok(Self {
            data: value.to_vec(),
        });
    }
}

impl Display for Matrix {
    fn fmt(&self, f: &mut Formatter<'_>) -> fmt::Result {
        let mut lines = vec![];

        for row in 0..MATRIX_HEIGHT {
            let items_in_row = self
                .get_row(row as u8)
                .into_iter()
                .map(|item| if item > 0u8 { "#" } else { "." })
                .collect::<Vec<&str>>()
                .join("");
            lines.push(items_in_row);
        }
        write!(f, "{}", lines.join("\r\n"))
    }
}

